#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	*str;

	i = 0;
	str = (char *)s;
	while (str[i] && str[i] != (unsigned char)c)
		i++;
	if (str[i] == '\0' && c != '\0')
		return (NULL);
	return (&str[i]);
}
