#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*str;
	size_t	str_len;
	int		i;

	i = 0;
	str_len = ft_strlen(s);
	str = malloc(sizeof(char) * str_len + 1);
	if (!str)
		return (NULL);
	while (s[i])
	{
		str[i] = s[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
